#include "../cub3d.h"

int	ft_skip_space(char *line, int *i)
{
	while (line[*i] == ' ' || line[*i] == '\t' || line[*i] == '\n'
	|| line[*i] == '\r' || line[*i] == '\v' || line[*i] == '\f')
		(*i)++;
	return (0);
}
